// File Object

package Project;

public class File {
	private String FileName ;
	
	public File() {
		this.FileName = FileName ;
	}	
	
	public String getFileName() {
		return FileName;
	}

	public void setFileName(String fileName) {
		FileName = fileName;
	}

	public String toString() {
		return this.FileName;
	}
}
